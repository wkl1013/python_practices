message = "Hello World!"
print(message)

# 修改字符串的大小写
print(message.upper())
print(message.lower())
print(message.lower().title())

# 字符串中使用变量
first_name = "Mike"
last_name = "Jack"
print(f"This people's name is {first_name} {last_name}")

# 删除空白 .rstrip() .lstrip() .strip()
favorite_language = " Python "
print(favorite_language.rstrip().lstrip())

# 数字的计算
print(10 ** 3)
# 数字中的下划线
print(14_000_000_000)